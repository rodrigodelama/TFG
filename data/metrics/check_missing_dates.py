# Sort dates
date_objects.sort()

# Find gaps
gaps = []
for i in range(1, len(date_objects)):
    delta = date_objects[i] - date_objects[i - 1]
    if delta.days > 1:
        for j in range(1, delta.days):
            gaps.append((date_objects[i - 1] + datetime.timedelta(days=j)).strftime("%Y-%m-%d"))

# Print gaps
print("Missing dates:")
for gap in gaps:
    print(gap)
